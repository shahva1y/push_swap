#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*elem;

	elem = malloc(sizeof(t_list));
	if (elem == (void *)0)
		return ((void *)0);
	elem->content = content;
	elem->next = (void *)0;
	return (elem);
}
